import requests
import time
import threading
import multiprocessing
import os
from typing import List, Tuple

def descargar_imagen(url: str, nombre_archivo: str):
    """Descarga una imagen desde una URL y la guarda con el nombre especificado"""
    try:
        response = requests.get(url, timeout=30)
        response.raise_for_status()
        
        with open(nombre_archivo, 'wb') as f:
            f.write(response.content)
        
        print(f"Descargada: {nombre_archivo}")
        return True
    except Exception as e:
        print(f"Error descargando {nombre_archivo}: {e}")
        return False

def descargar_secuencial(lista_urls: List[Tuple[str, str]]):
    """Descarga todas las imágenes de forma secuencial"""
    print("=== DESCARGA SECUENCIAL ===")
    inicio = time.time()
    
    for url, nombre in lista_urls:
        descargar_imagen(url, nombre)
    
    fin = time.time()
    tiempo_total = fin - inicio
    print(f"Tiempo total secuencial: {tiempo_total:.2f} segundos")
    return tiempo_total

def descargar_grupo_hilos(grupo_urls: List[Tuple[str, str]]):
    """Descarga un grupo de imágenes (para uso con hilos)"""
    for url, nombre in grupo_urls:
        descargar_imagen(url, nombre)

def descargar_con_hilos(lista_urls: List[Tuple[str, str]], num_hilos: int = 4):
    """Descarga imágenes usando hilos"""
    print(f"\n=== DESCARGA CON {num_hilos} HILOS ===")
    
    # Dividir la lista en grupos para cada hilo
    tamano_grupo = len(lista_urls) // num_hilos
    grupos = []
    
    for i in range(num_hilos):
        inicio = i * tamano_grupo
        fin = inicio + tamano_grupo if i < num_hilos - 1 else len(lista_urls)
        grupos.append(lista_urls[inicio:fin])
    
    hilos = []
    inicio = time.time()
    
    for i, grupo in enumerate(grupos):
        hilo = threading.Thread(target=descargar_grupo_hilos, args=(grupo,))
        hilos.append(hilo)
        hilo.start()
        print(f"Hilo {i+1} iniciado con {len(grupo)} imágenes")
    
    for hilo in hilos:
        hilo.join()
    
    fin = time.time()
    tiempo_total = fin - inicio
    print(f"Tiempo total con hilos: {tiempo_total:.2f} segundos")
    return tiempo_total

def descargar_grupo_procesos(grupo_urls: List[Tuple[str, str]]):
    """Descarga un grupo de imágenes (para uso con procesos)"""
    for url, nombre in grupo_urls:
        descargar_imagen(url, nombre)

def descargar_con_procesos(lista_urls: List[Tuple[str, str]], num_procesos: int = 4):
    """Descarga imágenes usando procesos"""
    print(f"\n=== DESCARGA CON {num_procesos} PROCESOS ===")
    
    # Dividir la lista en grupos para cada proceso
    tamano_grupo = len(lista_urls) // num_procesos
    grupos = []
    
    for i in range(num_procesos):
        inicio = i * tamano_grupo
        fin = inicio + tamano_grupo if i < num_procesos - 1 else len(lista_urls)
        grupos.append(lista_urls[inicio:fin])
    
    procesos = []
    inicio = time.time()
    
    for i, grupo in enumerate(grupos):
        proceso = multiprocessing.Process(target=descargar_grupo_procesos, args=(grupo,))
        procesos.append(proceso)
        proceso.start()
        print(f"Proceso {i+1} iniciado con {len(grupo)} imágenes")
    
    for proceso in procesos:
        proceso.join()
    
    fin = time.time()
    tiempo_total = fin - inicio
    print(f"Tiempo total con procesos: {tiempo_total:.2f} segundos")
    return tiempo_total

def limpiar_archivos_descargados():
    """Elimina los archivos descargados para poder repetir las pruebas"""
    for _, nombre in urls:
        if os.path.exists(nombre):
            os.remove(nombre)
            print(f"Eliminado: {nombre}")

def main():
    """Función principal que ejecuta todas las versiones de descarga"""
    
    # Limpiar archivos previos
    print("Limpiando archivos previos...")
    limpiar_archivos_descargados()
    
    print(f"Total de imágenes a descargar: {len(urls)}")
    
    # Descarga secuencial
    tiempo_secuencial = descargar_secuencial(urls)
    
    # Limpiar para siguiente prueba
    print("\nLimpiando archivos para siguiente prueba...")
    limpiar_archivos_descargados()
    
    # Descarga con hilos
    tiempo_hilos = descargar_con_hilos(urls, 4)
    
    # Limpiar para siguiente prueba
    print("\nLimpiando archivos para siguiente prueba...")
    limpiar_archivos_descargados()
    
    # Descarga con procesos
    tiempo_procesos = descargar_con_procesos(urls, 4)
    
    # Comparación final
    print("\n" + "="*50)
    print("COMPARACIÓN FINAL DE TIEMPOS")
    print("="*50)
    print(f"Tiempo secuencial: {tiempo_secuencial:.2f} segundos")
    print(f"Tiempo con 4 hilos: {tiempo_hilos:.2f} segundos")
    print(f"Tiempo con 4 procesos: {tiempo_procesos:.2f} segundos")
    
    # Cálculo de speedup
    if tiempo_hilos > 0:
        speedup_hilos = tiempo_secuencial / tiempo_hilos
        print(f"SpeedUp con hilos: {speedup_hilos:.2f}x")
    
    if tiempo_procesos > 0:
        speedup_procesos = tiempo_secuencial / tiempo_procesos
        print(f"SpeedUp con procesos: {speedup_procesos:.2f}x")

if __name__ == "__main__":
    main()

